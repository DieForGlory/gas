def seed_geodata(db: Session):
    for region_name, districts in GEODATA.items():
        region = db.query(models.Region).filter(models.Region.name == region_name).first()
        if not region:
            region = models.Region(name=region_name)
            db.add(region)
            db.flush()

        for district_name in districts:
            district = db.query(models.District).filter(
                models.District.name == district_name,
                models.District.region_id == region.id
            ).first()
            if not district:
                db.add(models.District(name=district_name, region_id=region.id))
    db.commit()


